#include <bits/stdc++.h>
using namespace std;


int main()
{
    long long int n;
    cin>>n;
    long long int i,m[n],sum=0;
    for( i=0;i<n-1;i++){
        cin>>m[i];
        sum+=m[i];
    }
    cout<<(n*(n+1)/2)-sum;
}

